function genGuidebook(t) {
  const generic = [`${t} 파트의 핵심 개념을 학습합니다.`, '주어진 문제의 지문을 꼼꼼히 읽고 조건에 맞게 코드를 설계하세요.', '💡 자주 틀리는 유형: 문법 기호(콜론, 따옴표, 괄호) 누락', '🚀 해결 팁: 에러 메시지 하단에 표시되는 힌트 문구를 적극 활용해보세요.'];
  return GUIDEBOOK[t] || generic;
}

function mcqStep(item) {
  return `{
    type: 'quiz_multiple_choice',
    content: '${escapeContent(item.q)}',
    options: ${JSON.stringify(item.opts)},
    answer: ${item.a},
    explanation: '파이썬 문법의 핵심 개념입니다.',
    hint: '${escapeContent(item.hint)}'
  }`;
}

function wbStep(item, idx) {
  return `{
    type: 'quiz_word_bank',
    content: '단어 블록 빈칸 채우기 (${idx + 1})',
    sentenceParts: ['${escapeContent(item.parts[0])}', '${escapeContent(item.parts[1])}'],
    wordBank: ${JSON.stringify(item.bank)},
    answer: ${JSON.stringify(item.ans.map(i => item.bank[i]))},
    hint: '${escapeContent(item.hint)}'
  }`;
}

function codeStep(item) {
  return `{
    type: 'quiz_code',
    content: '${escapeContent(item.c)}',
    initialCode: '${escapeContent(item.i)}',
    expectedOutputs: ['${escapeContent(item.e)}'],
    hint: '${escapeContent(item.h)}'
  }`;
}

// Splits each topic's 5 MCQ + 5 word-bank + 10 code items (already authored
// easy -> hard) across the node's 4 lesson-attempts so every lesson is a
// distinct, non-duplicated subset and difficulty genuinely escalates from
// Lesson 1 (recognition/typo-fix) to Lesson 4 (self-write challenge).
function buildLesson(topic, mcqRange, wbRange, codeRange) {
  const steps = [];
  for (let i = mcqRange[0]; i < mcqRange[1]; i++) steps.push(mcqStep(topic.mcq[i]));
  for (let i = wbRange[0]; i < wbRange[1]; i++) steps.push(wbStep(topic.wb[i], i));
  for (let i = codeRange[0]; i < codeRange[1]; i++) steps.push(codeStep(topic.code[i]));
  return steps;
}

function buildNodeLessons(t) {
  const topic = { mcq: MCQ[t], wb: BANK[t], code: CODE[t] };
  const lesson1 = buildLesson(topic, [0, 1], [0, 1], [0, 3]);   // easiest
  const lesson2 = buildLesson(topic, [1, 2], [1, 2], [3, 5]);   // basic application
  const lesson3 = buildLesson(topic, [2, 4], [2, 4], [5, 8]);   // intermediate / combination
  const lesson4 = buildLesson(topic, [4, 5], [4, 5], [8, 10]);  // hardest: challenge / self-write
  return [lesson1, lesson2, lesson3, lesson4];
}

